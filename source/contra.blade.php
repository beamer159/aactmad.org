                <h1 class="title">Contra Dancing in Ann Arbor</h1>

                <div class="box">
                    <div>There's a contra dance in Ann Arbor almost every Saturday night. Some are AACTMAD events. Some
                        are put on by other organizations. All are fun. Most dances begin at 7:30 or 8:00pm.
                    </div>
                    <div><strong>Beginners are welcome:</strong> Unless otherwise noted in an annoucement, beginners are
                        always welcome. Often there is an introductory session 30 minutes before the dance, so
                        first-timers come early. Come alone or bring a friend. Anyone can ask anyone else to dance;
                        experienced dancers are happy to help new people. It's a common practice to change partners
                        after each dance sequence, and it works well while you're learning the basics; after that, it's
                        up to you. Wear cool, comfortable clothes and flat shoes that aren't sticky.
                    </div>

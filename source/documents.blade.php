                <h1>AACTMAD Documents</h1>

                <p>AACTMAD is an all-volunteer membership organization that depends on the experience, skills, energy,
                    and vision of many people who care about traditional music and dance. The board of directors is
                    committed to the long-range or strategic goals of AACTMAD, sets the direction for the organization,
                    and oversees its activities and services.</p>

                <p>Consistent with the Carver Model for nonprofit management, the board is focused on the "ends" (the
                    "what and why"). The work of the organization is carried out by its committees which are focused on
                    the "means" (the "how"). The AACTMAD Guide includes job descriptions.</p>


                <h2>Financial and Budgeting</h2>

                <p>As a public foundation, we are required to allow public access to our financial records. Standard
                    financial reporting includes balance sheets, income statements, and cash flow or changes in fund
                    balance statements. Directors use these reports in making decisions about budgets, grants, etc.
                    Other organizations that we approach for financial assistance require that they be clear, accurate,
                    and timely. All income and expenditures are reported on the Income and Expense forms.</p>

                <p>Although AACTMAD&rsquo;s fiscal year is the calendar year, our budget process runs from September
                    through August, which we call a "season." Financial reports cover January through December; budget
                    documents cover September through August.</p>

                <p>The board decides on the best uses of the available financial resources through its annual budgeting
                    process. All ongoing committees submit an Annual Report and Request for Resources (ARRR) form by May
                    31. Submitting this form is the way to obtain AACTMAD's commitment to funding or guaranteeing
                    funding for the coming year.</p>

                <h2>Special Grants</h2>

                <p>During the budgeting process, the board usually sets aside an amount for discretionary spending that
                    it can allocate for needs that arise after the new season has begun. The board encourages new ideas
                    and supports the enthusiasm of volunteers who are encouraged to try new projects and services. The
                    Event - How To document contains useful background information to assist and support organizers of
                    new projects.</p>

